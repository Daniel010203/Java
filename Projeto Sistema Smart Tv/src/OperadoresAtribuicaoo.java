public class OperadoresAtribuicaoo {
    public static void main(String[] args) {
        int x = 10;
        x = x + 15;
        System.out.println(x);

        int y = 10;
        y += 15;
        System.out.println(y);

        int z = 500;
        z /= 15;
        System.out.println(z);

        int b = 200;
        b *= 15;
        System.out.println(b);
    }
}
